export interface Duty {
  id: string;
  category: string;
  jobTitle: string;
  lastName: string;
  rank: string;
  dateStarted: string;
  termLength: string;
  termEndDate: string;
  elementOrGroup: string;
  tierLevel: number | null; // null if N/A or empty
  specialized: boolean; // true if SPEC/TITLE, false if N
  dutyType: "EL" | "U" | "N/A"; // Element or Unit duty
  isCommandAppointed?: boolean; // true if command appointed (blue cell)
}

export function parseRawDuties(): Duty[] {
  const lines = RAW_DUTIES_TSV.split("\n");
  const duties: Duty[] = [];
  let currentCategory = "";

  let idCounter = 1;

  for (const line of lines) {
    const trimmed = line.trim();
    if (!trimmed) continue;

    const cols = line.split("\t").map(col => col.trim());
    
    // Check if it's a category header
    // Rule: first column has text, all other columns are empty
    const firstColHasText = cols[0] !== "";
    const otherColsEmpty = cols.slice(1).every(col => col === "");

    if (firstColHasText && otherColsEmpty) {
      currentCategory = cols[0];
      continue;
    }

    // Otherwise, it's a duty row if first column is not empty or second is not empty
    if (cols[0] !== "" || (cols[1] !== "" && cols[1] !== undefined)) {
      const jobTitle = cols[0] || "";
      const lastName = cols[1] || "";
      const rank = cols[2] || "";
      const dateStarted = cols[3] || "";
      const termLength = cols[4] || "";
      const termEndDate = cols[5] || "";
      const elementOrGroup = cols[6] || "";
      
      const rawTier = cols[7] || "";
      let tierLevel: number | null = null;
      if (rawTier && rawTier !== "N/A") {
        const parsed = parseInt(rawTier, 10);
        if (!isNaN(parsed)) {
          tierLevel = parsed;
        }
      }

      const specialized = cols[8] === "SPEC/TITLE";
      
      const rawType = cols[9] || "N/A";
      let dutyType: "EL" | "U" | "N/A" = "N/A";
      if (rawType === "EL" || rawType === "U" || rawType === "N/A") {
        dutyType = rawType;
      }

      const cat = currentCategory || "General";

      duties.push({
        id: "duty_" + idCounter++,
        category: cat,
        jobTitle,
        lastName,
        rank,
        dateStarted,
        termLength,
        termEndDate,
        elementOrGroup,
        tierLevel,
        specialized,
        dutyType,
        isCommandAppointed: isCommandAppointedDuty(cat, jobTitle),
      });
    }
  }

  return duties;
}

